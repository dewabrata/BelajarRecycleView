package com.juara.belajarrecycleview;

public class ModelBelajar {

    private String namaKelas;
    private String jamKelas;
    private String tglKelas;


    public ModelBelajar() {
    }

    public String getNamaKelas() {
        return namaKelas;
    }

    public void setNamaKelas(String namaKelas) {
        this.namaKelas = namaKelas;
    }

    public String getJamKelas() {
        return jamKelas;
    }

    public void setJamKelas(String jamKelas) {
        this.jamKelas = jamKelas;
    }

    public String getTglKelas() {
        return tglKelas;
    }

    public void setTglKelas(String tglKelas) {
        this.tglKelas = tglKelas;
    }
}
