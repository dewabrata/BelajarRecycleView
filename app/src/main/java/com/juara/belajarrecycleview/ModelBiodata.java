package com.juara.belajarrecycleview;

public class ModelBiodata {

    private String nama;
    private String alamat;

    public ModelBiodata() {
    }

    public String getNama() {
        return nama;
    }

    public void setNama(String nama) {
        this.nama = nama;
    }

    public String getAlamat() {
        return alamat;
    }

    public void setAlamat(String alamat) {
        this.alamat = alamat;
    }
}
